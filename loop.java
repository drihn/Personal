 public class Loop {

        public static void main(String[] args) throws Exception {
    
         
    
            char[][] Array_3x3 = {
    
                {'A', 'B', 'C'},
    
                {'D', 'E', 'F'},
    
                {'G', 'H', 'I'}
    
    };
    
            for (int i = 0; i < Array_3x3.length; i++){
    
                for (int j = 0; j < Array_3x3.length; j++){
    
                        System.out.print(Array_3x3[i][j]);
    
                }
    
            System.out.print("\n");
    
            }
    
        }
    
    }
